-- 4. В таблице Н_ГРУППЫ_ПЛАНОВ найти номера планов,
--   по которым обучается (обучалось) менее 2 групп на очной форме обучения.
--   Для реализации использовать соединение таблиц.

SELECT Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
FROM Н_ФОРМЫ_ОБУЧЕНИЯ
JOIN Н_ПЛАНЫ ON Н_ФОРМЫ_ОБУЧЕНИЯ.ИД = Н_ПЛАНЫ.ФО_ИД
JOIN Н_ГРУППЫ_ПЛАНОВ ON Н_ПЛАНЫ.ПЛАН_ИД = Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
WHERE Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Очная'
GROUP BY Н_ГРУППЫ_ПЛАНОВ.ПЛАН_ИД
HAVING COUNT(Н_ГРУППЫ_ПЛАНОВ.ГРУППА) < 2;
