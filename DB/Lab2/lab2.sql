-- 6. Получить список студентов, отчисленных ровно первого сентября 2012 года с заочной формы обучения. В результат включить:
--   номер группы;
--   номер, фамилию, имя и отчество студента;
--   номер пункта приказа;
--   Для реализации использовать подзапрос с EXISTS.

SELECT Н_УЧЕНИКИ.ГРУППА, Н_УЧЕНИКИ.ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО, Н_УЧЕНИКИ.П_ПРКОК_ИД, Н_УЧЕНИКИ.КОНЕЦ_ПО_ПРИКАЗУ
FROM Н_УЧЕНИКИ
JOIN Н_ЛЮДИ ON Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
WHERE EXISTS (
     SELECT 1 FROM Н_УЧЕНИКИ
     JOIN Н_ПЛАНЫ ON Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД
     JOIN Н_ФОРМЫ_ОБУЧЕНИЯ ON Н_ПЛАНЫ.ФО_ИД = Н_ФОРМЫ_ОБУЧЕНИЯ.ИД
     WHERE Н_ФОРМЫ_ОБУЧЕНИЯ.НАИМЕНОВАНИЕ = 'Очная'
)
AND Н_УЧЕНИКИ.КОНЕЦ_ПО_ПРИКАЗУ::DATE = '2012-08-31'; -- преобразование типов
-- ============================================================


-- ============================================================
-- 7. Вывести список студентов, имеющих одинаковые имена, но не совпадающие даты рождения.
-- Уник пары (имя + дата)

SELECT DISTINCT Л1.ИМЯ, Л1.ДАТА_РОЖДЕНИЯ
FROM "Н_ЛЮДИ" Л1
WHERE EXISTS (
    SELECT 1 FROM "Н_УЧЕНИКИ" У
    WHERE У.ЧЛВК_ИД = Л1.ИД
)
AND Л1.ИМЯ IN (
    SELECT ИМЯ
    FROM "Н_ЛЮДИ"
    GROUP BY ИМЯ
    HAVING COUNT(DISTINCT ДАТА_РОЖДЕНИЯ) > 1
)
ORDER BY Л1.ИМЯ;


