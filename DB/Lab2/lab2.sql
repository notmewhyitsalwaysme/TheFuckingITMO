-- 5. Выведите таблицу со средними оценками студентов группы 4100 (Номер, ФИО, Ср_оценка),
--  у которых средняя оценка не больше максимальной оценк(е|и) в группе 3100.

SELECT
    Н_УЧЕНИКИ.ЧЛВК_ИД AS Номер,
	Н_ЛЮДИ.ФАМИЛИЯ ||' '|| Н_ЛЮДИ.ИМЯ || ' ' || Н_ЛЮДИ.ОТЧЕСТВО AS ФИО,
	ROUND(AVG(Н_ОЦЕНКИ.СОРТ), 2) AS Ср_оценка
FROM Н_УЧЕНИКИ
JOIN Н_ЛЮДИ ON Н_ЛЮДИ.ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
JOIN Н_ОБУЧЕНИЯ  ON Н_ОБУЧЕНИЯ.ЧЛВК_ИД  = Н_ЛЮДИ.ИД
JOIN Н_ВЕДОМОСТИ ON Н_ВЕДОМОСТИ.ЧЛВК_ИД  = Н_ЛЮДИ.ИД
JOIN Н_ОЦЕНКИ ON Н_ОЦЕНКИ.КОД = Н_ВЕДОМОСТИ.ОЦЕНКА
WHERE Н_УЧЕНИКИ.ГРУППА = '4100'
GROUP BY Н_УЧЕНИКИ.ЧЛВК_ИД, Н_ЛЮДИ.ФАМИЛИЯ, Н_ЛЮДИ.ИМЯ, Н_ЛЮДИ.ОТЧЕСТВО
HAVING ROUND(AVG(Н_ОЦЕНКИ.СОРТ), 2) <= (
    SELECT MAX(round) from ( -- 3.49 максимум
		SELECT ROUND(AVG(Н_ОЦЕНКИ.СОРТ), 2), Н_УЧЕНИКИ.ЧЛВК_ИД
		FROM Н_УЧЕНИКИ
		JOIN Н_ВЕДОМОСТИ ON Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
		JOIN Н_ОЦЕНКИ ON Н_ОЦЕНКИ.КОД = Н_ВЕДОМОСТИ.ОЦЕНКА
		WHERE Н_УЧЕНИКИ.ГРУППА = '3100' AND Н_ОЦЕНКИ.СОРТ < 5
		GROUP BY Н_УЧЕНИКИ.ЧЛВК_ИД
	)
);
